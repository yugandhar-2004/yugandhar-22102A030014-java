class Arithmatic
{
    public static void main(String args[])
    {
        addition(2,3);
        subtraction(3,2);
        multiplication(2,3);
    }
    static void addition(int a,int b)
    {
        System.out.println(a+b);
    }
    static void subtraction(int a,int b)
    {
        System.out.println(a-b);
    }
    static void multiplication(int a,int b)
    {
        System.out.println(a*b);
    }
}